package po;

public class Anesthetist {
 private String Anethetist_id;
 private String password;
 private String name;
 private int age;
 private int gender;
public String getAnethetist_id() {
	return Anethetist_id;
}
public void setAnethetist_id(String anethetist_id) {
	Anethetist_id = anethetist_id;
}
public String getPassword() {
	return password;
}
public void setPassword(String password) {
	this.password = password;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}
public int getGender() {
	return gender;
}
public void setGender(int gender) {
	this.gender = gender;
}
 
 
	
}
